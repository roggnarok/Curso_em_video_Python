#Conversor de temperatura
celcius = float(input('Informe a temperatura em °C: '))
kelvin = celcius + 273.15
faren = (celcius * 1.8) + 32
print(f'A temperatura convertida para Fahrenheit é de {faren:.1f} °F')
print(f'A temperatura convertida para Kelvin é de {kelvin:.2f} K')
