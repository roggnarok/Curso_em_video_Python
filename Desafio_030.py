'''
Crie um programa que leia um número inteiro e mostre na tela se ele é PAR ou IMPAR
'''
numero = int(input('Digite um número: '))
if (numero % 2) == 0 : #Se for PAR, faça:
    print('O número é PAR.')
else:
    print('O número é IMPAR.')
