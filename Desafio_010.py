#A pessoa informa quanto dinheiro tem na carteira, a cotação do Dólar e o programa calculará quantos dólares ela pode comprar
dinheiro = float(input('Quanto de dinheiro você tem? '))
dolar = float(input('Qual é a cotação do Dóllar hoje? '))
compra = dinheiro / dolar
print(f'Você poderá comprar {compra :.2f} Dolares!')
